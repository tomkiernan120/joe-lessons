// below is a list of operators

// Assignment Operators
// these operators assign a value to a variable

// set a variable to 5
var x = 5;

// set a variable to 5 + 5
var y = 5 + 5;

// set a variable to 5 * 5
var z = 5 * 5;

// set a variable to 5 / 5
var a = 5 / 5;

// set a variable to 5 - 5
var b = 5 - 5;

// set a variable to 5 % 5
var c = 5 % 5;

// set a variable to 5 to the power of 5
var d = 5 ** 5;

// set a variable to 5
var e = 5;

// set a variable to 5 + 5
e += 5;

// set a variable to 5 - 5
e -= 5;

// set a variable to 5 * 5
e *= 5;

// set a variable to 5 / 5
e /= 5;

// set a variable to 5 % 5
e %= 5;

// set a variable to 5 to the power of 5
e **= 5;

// Comparison Operators
// these operators compare two values and return true or false

// loose comparison
var f = 5 == 5;

// strict comparison
var g = 5 === 5;

// loose not equal comparison
var h = 5 != 5;

// strict not equal comparison
var i = 5 !== 5;

// greater than comparison
var j = 5 > 5;

// greater than or equal to comparison
var k = 5 >= 5;

// less than comparison
var l = 5 < 5;

// less than or equal to comparison
var m = 5 <= 5;

// Logical Operators
// these operators are used to determine the logic between variables and values

// and operator
var n = 5 && 5;

// or operator
var o = 5 || 5;

// not operator
var p = !5;

// String Operators
// these operators are used to manipulate strings

// concatenation operator
var x = 'Hello' + 'World';

// string interpolation operator
var y = `Hello ${x}`;

// typeof operator
var z = typeof x;

// instanceof operator
var a = x instanceof String;

// Conditional (Ternary) Operator
// this operator is used to assign a value based on a condition

// ternary operator
var b = x > 5 ? 'Hello' : 'World';

// delete operator
// this operator is used to delete a property from an object

// delete operator
delete x;

// in operator
// this operator is used to check if a property exists in an object

// in operator
'x' in x;

// new operator
// this operator is used to create a new object

// new operator
new String('Hello');